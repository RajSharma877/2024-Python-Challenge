# Arithmetic operators
a = 5
b = 2
c = a + b
print(c)

# Assignment operators
a = 4 - 2  # Assign 4-2 to a
print(a)
b = 6
b += 5  # Increment b by 5 and then assign it to b
print(b)

# Comparison operators
d = 5 == 5  # = is an assignment operator and == is a comparison operator
print(d)

# Logical operators
e = True or False

# Truth table of or

